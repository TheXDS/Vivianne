namespace TheXDS.Vivianne.Data;

/// <summary>
/// Enumerates all the known file types.
/// </summary>
public enum KnownFileType
{
    /// <summary>
    /// Viv file.
    /// </summary>
    Viv,
    /// <summary>
    /// FCE 3D model.
    /// </summary>
    Fce,
    /// <summary>
    /// GEO 3D model.
    /// </summary>
    Geo3DModel,
    /// <summary>
    /// Sound Bank file.
    /// </summary>
    Bnk,
    /// <summary>
    /// ASF music track.
    /// </summary>
    Asf,
    /// <summary>
    /// Common picture formats.
    /// </summary>
    Picture,
    /// <summary>
    /// SHPI texture.
    /// </summary>
    Shpi,
    /// <summary>
    /// Front-end data/language file.
    /// </summary>
    FeData,
    /// <summary>
    /// Car performance data.
    /// </summary>
    Carp,
    /// <summary>
    /// Interactive music file.
    /// </summary>
    Mus,
    /// <summary>
    /// ReadMe file.
    /// </summary>
    ReadMe,
}
