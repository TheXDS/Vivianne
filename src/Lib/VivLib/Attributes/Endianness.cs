namespace TheXDS.Vivianne.Attributes;

/// <summary>
/// Indicates the endianess of a numeric field
/// </summary>
public enum Endianness
{
    /// <summary>
    /// Indicates that the field is stored in little-endian.
    /// </summary>
    LittleEndian,

    /// <summary>
    /// Indicates that the field is stored in big-endian.
    /// </summary>
    BigEndian
}
